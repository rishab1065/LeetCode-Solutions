/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var threeSum = function (nums) {
  let triplatesMap = {};
  nums = nums.sort((a, b) => a - b);
  for (var firstNumIndex = 0; firstNumIndex < nums.length; firstNumIndex++) {
    var secondNumIndex = firstNumIndex + 1;
    var thirdNumIndex = nums.length - 1;
    var visitedNumbers = {};
    while (secondNumIndex < thirdNumIndex) {
      if (visitedNumbers[nums[secondNumIndex]]) {
        // console.log('Dfdfg');
        secondNumIndex++;
        continue;
      }
      if (visitedNumbers[nums[thirdNumIndex]]) {
        // console.log('Dfdfg');
        thirdNumIndex--;
        continue;
      }
      const sum =
        nums[secondNumIndex] + nums[thirdNumIndex] + nums[firstNumIndex];
      if (sum === 0) {
        const triplates = [
          nums[secondNumIndex],
          nums[thirdNumIndex],
          nums[firstNumIndex],
        ];
        visitedNumbers[nums[secondNumIndex]] = true;
        visitedNumbers[nums[thirdNumIndex]] = true;
        triplatesMap[JSON.stringify(triplates.sort())] = triplates;
        secondNumIndex++;
        thirdNumIndex--;
      } else if (sum < 0) {
        secondNumIndex++;
      } else {
        thirdNumIndex--;
      }
    }
  }
  return Object.values(triplatesMap);
};

